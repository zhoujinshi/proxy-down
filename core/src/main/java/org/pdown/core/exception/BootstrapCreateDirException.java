package org.pdown.core.exception;

public class BootstrapCreateDirException extends BootstrapException {

  public BootstrapCreateDirException() {
    super();
  }

  public BootstrapCreateDirException(String message) {
    super(message);
  }

  public BootstrapCreateDirException(String message, Throwable cause) {
    super(message, cause);
  }

  public BootstrapCreateDirException(Throwable cause) {
    super(cause);
  }
}
