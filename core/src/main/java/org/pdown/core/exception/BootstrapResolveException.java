package org.pdown.core.exception;

public class BootstrapResolveException extends BootstrapBuildException {

  public BootstrapResolveException() {
    super();
  }

  public BootstrapResolveException(String message) {
    super(message);
  }

  public BootstrapResolveException(String message, Throwable cause) {
    super(message, cause);
  }

  public BootstrapResolveException(Throwable cause) {
    super(cause);
  }
}
